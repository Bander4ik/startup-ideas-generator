export async function chatWithContext(messages, ideaContext = null) {
  try {
    const systemMessage = ideaContext 
      ? `${SYSTEM_PROMPT}\n\n–ö–æ–Ω—Ç–µ–∫—Å—Ç –ø–æ—Ç–æ—á–Ω–æ—ó —ñ–¥–µ—ó:\n${ideaContext}\n\n–í—ñ–¥–ø–æ–≤—ñ–¥–∞–π –Ω–∞ –ø–∏—Ç–∞–Ω–Ω—è –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –≤ –∫–æ–Ω—Ç–µ–∫—Å—Ç—ñ —Ü—ñ—î—ó —ñ–¥–µ—ó. –ë—É–¥—å –∫–æ–Ω–∫—Ä–µ—Ç–Ω–∏–º, –∞–Ω–∞–ª—ñ—Ç–∏—á–Ω–∏–º —Ç–∞ –∫–æ—Ä–∏—Å–Ω–∏–º.`
      : SYSTEM_PROMPT;

    const completion = await groq.chat.completions.create({
      messages: [
        { role: 'system', content: systemMessage },
        ...messages
      ],
      model: 'llama-3.3-70b-versatile',
      temperature: 0.6,
      max_tokens: 2048,
      top_p: 0.9
    });

    return completion.choices[0]?.message?.content || '';
  } catch (error) {
    console.error('Groq API Error:', error);
    throw new Error('–ü–æ–º–∏–ª–∫–∞ –æ–±—Ä–æ–±–∫–∏ –∑–∞–ø–∏—Ç—É');
  }
}

